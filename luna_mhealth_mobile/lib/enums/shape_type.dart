/// An enumeration of shape types.
///
/// This enum is used to distinguish between different types of shapes.
/// Currently, it supports rectangles, circles, and triangles.

enum ShapeType {
  /// Represents a rectangle shape.
  rectangle,

  /// Represents a circle shape.
  circle,

  /// Represents a triangle shape.
  triangle
}
