/// =================================================================================================
/// ODP XML ELEMENTS CONSTANTS
/// =================================================================================================
/// The XML element for the document containing metadata.
const String eMetaDocument = 'office:document-meta';

/// The XML element for the metadata.
const String eMeta = 'office:meta';

/// The XML element for the title.
const String eTitle = 'dc:title';

/// The XML element for the author.
const String eAuthor = 'meta:initial-creator';

/// The XML element for the document containing the styles of an ODP file.
const String eStylesDocument = 'office:document-styles';

/// The XML element for master styles.
const String eMasterStyles = 'office:master-styles';

/// The XML element for the master page.
const String eMasterPage = 'style:master-page';

/// The XML element for the page layout name.
const String ePageLayoutName = '_style:page-layout-name';

/// The XML element for automatic styles.
const String eAutomaticStyles = 'office:automatic-styles';

/// The XML element for page layout.
const String ePageLayout = 'style:page-layout';

/// The XML element for page layout properties.
const String ePageLayoutProperties = 'style:page-layout-properties';

/// The XML element for the page width.
const String ePageWidth = '_fo:page-width';

/// The XML element for the page width.
const String ePageHeight = '_fo:page-height';

/// The XML element for the document containing the content of an ODP file.
const String eContentDocument = 'office:document-content';

/// The XML element for the content body.
const String eBody = 'office:body';

/// The XML element for the presentation content.
const String ePresentation = 'office:presentation';

/// The XML element for a page in the presentation.
const String ePage = 'draw:page';

/// The XML element for a connection shape (line) in the ODP file.
const String eConnectionShape = 'draw:line';

/// The XML element for the start X coordinate of the connection shape.
const String eX1 = '_svg:x1';

/// The XML element for the start Y coordinate of the connection shape.
const String eY1 = '_svg:y1';

/// The XML element for the end X coordinate of the connection shape.
const String eX2 = '_svg:x2';

/// The XML element for the end Y coordinate of the connection shape.
const String eY2 = '_svg:y2';